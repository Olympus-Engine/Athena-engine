package fr.mrqsdf.olympus.register;

/**
 * Types of registrations available.
 */
public enum RegisterType {

    /**
     * Represents the registration of costs for competences in the system.
     */
    COST,
    /**
     * Represents the registration of competences in the system.
     */
    COMPETENCE,

    /**
     * Represents the registration of prerequisites for competences in the system.
     */
    PREREQUISITE,

    /**
     * Represents the registration of all types in the system.
     */
    ALL
}
